CREATE PROCEDURE UPDATE_DATA_TEST2
(
	@ID INT,
	@COLUMN Char(1)
)
AS
BEGIN
UPDATE #TEST2  
SET A = CASE @COLUMN  
WHEN 'A' THEN ~A
ELSE A 
END,
B = CASE @COLUMN  
WHEN 'B' THEN ~B
ELSE B
END,
C = CASE @COLUMN  
WHEN 'C' THEN ~C
ELSE C
END,
D = CASE @COLUMN  
WHEN 'D' THEN ~D
ELSE D
END,
E = CASE @COLUMN  
WHEN 'E' THEN ~E
ELSE E
END
WHERE ID=@ID
END

